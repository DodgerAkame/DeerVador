package dark.bambi;

public class Hero {

	private int max_jauge;
	private int jauge;
	private int level;
	
	public int getLevel() {
		return level;
	}
	public void setLevel(int level) {
		this.level = level;
	}
	public int getMax_jauge() {
		return max_jauge;
	}
	public void setMax_jauge(int max_jauge) {
		this.max_jauge = max_jauge;
	}
	public int getJauge() {
		return jauge;
	}
	public void setJauge(int jauge) {
		this.jauge = jauge;
	}
	
}
